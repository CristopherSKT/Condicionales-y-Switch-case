let angulo = parseFloat(prompt("Ingrese el primer angulo a sumar"))
let angulo2 = parseFloat(prompt("Ingrese el segundo angulo a sumar"))
let angulo3 = parseFloat(prompt("Ingrese el tercer angulo a sumar"))
if (angulo+angulo2+angulo3 == 180){
console.log("Es un triangulo")
}else{
    console.log("No es un triangulo")
}
